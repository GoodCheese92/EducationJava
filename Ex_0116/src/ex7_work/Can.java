package ex7_work;

public class Can {
	private String name;
	private int price;
	
	public Can() {
		
	}
	
	public Can(String name, int price) {
		this.name = name;
		this.price = price;		
	} // end of constructor
	
	public String getName() {
		return name;
	}
	
	public int getPrice() {
		return price;
	}
	
} // end of class
